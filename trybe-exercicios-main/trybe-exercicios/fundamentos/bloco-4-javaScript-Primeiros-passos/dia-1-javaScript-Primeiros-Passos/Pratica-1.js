const a = 10;
const b = 20;
//Soma
let soma = a + b
console.log(`Soma = ${soma}`)
//Subtração
let subtracao = a - b
console.log(`Subtração = ${subtracao}`)
//Multiplicação
let multiplicacao = a * b
console.log(`Multiplicação = ${multiplicacao}`)
//Divisão
let divisao = a / b
console.log(`Divisão = ${divisao}`)
//Modulo
let restoDivisao = a % b
console.log(`Modulo = ${restoDivisao}`)